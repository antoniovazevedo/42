int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;

	i = 0;
	while (i < n && (*s1 != '\0' || *s2 != '\0'))
	{
		if (*s1 != *s2)
			return (*s1 - *s2);
		if (*s1 != '\0')
			s1++;
		if (*s2 != '\0')
			s2++;
		i++;
	}
	return (0);
}
