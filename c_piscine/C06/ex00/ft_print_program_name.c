#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str)
	{
		write(1, str, 1);
		str++;
	}
}

int		main(int arg_count, char **args)
{
	ft_putstr(args[0]);
	ft_putstr("\n");
}
