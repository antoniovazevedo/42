char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (i >= 0)
	{
		dest[i] = *src;
		if (*src == '\0')
		{
			break ;
		}
		src++;
		i++;
	}
	return (dest);
}
